    private void setDropTarget() {
        DropTarget dt = new DropTarget(list, new DropTargetAdapter() {

            @Override
            public void dragEnter(DropTargetDragEvent dtde) {
                if (!dtde.isDataFlavorSupported(ModelBase.getNodeFlavor())) {
                    dtde.rejectDrag();
                } else {
                    try {
                        SeqRun sr = (SeqRun) dtde.getTransferable().getTransferData(ModelBase.getNodeFlavor());
                        if (sr == null || data.getSeqRuns().contains(sr)) {
                            dtde.rejectDrag();
                        }
                    } catch (Exception ex) {
                        Exceptions.printStackTrace(ex);
                        dtde.rejectDrag();
                    }
                }
            }

            @Override
            public void drop(DropTargetDropEvent dtde) {
                try {
                    SeqRun sr = (SeqRun) dtde.getTransferable().getTransferData(ModelBase.getNodeFlavor());
                    if (data.getSeqRuns().contains(sr)) {
                        dtde.rejectDrop();
                    }
                    addSeqRun(sr);
                } catch (Exception ex) {
                    Exceptions.printStackTrace(ex);
                    dtde.rejectDrop();
                }
            }
        });
        setDropTarget(dt);
    }

