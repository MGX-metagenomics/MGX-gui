    public Pair<PC, PC> getPCs() {
        String s = (String) pcSel.getSelectedItem();
        switch (s) {
            case "PC1 / PC2":
                return pc12;
            case "PC2 / PC3":
                return pc23;
            case "PC1 / PC3":
                return pc13;
        };
        return pc12;
    }
    
