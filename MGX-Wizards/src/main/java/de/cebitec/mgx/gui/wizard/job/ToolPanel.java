    public ToolPanel setTool(ToolI t) {
        tool = t;
        StringBuilder name = new StringBuilder();
        if (tool.getUrl() != null && !"".equals(tool.getUrl())) {
            name.append("<html><u>").append(tool.getName()).append("</u></html>");
        } else {
            name.append(tool.getName());
        }
        name.append(" (").append(tool.getVersion()).append(")");

        toolname.setText(name.toString());
        authors.setText(t.getAuthor());
        description.setText(t.getDescription());
        
        return this;
    }
    
    public ToolI getTool() {
        return tool;
    }

